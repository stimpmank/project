package org.profamilia.registro.model.entities;

public class Chzona {

    public String id;

    public String descripcion;

    public Chzona() {
    }


    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getDescripcion() {
        return descripcion;
    }
}
