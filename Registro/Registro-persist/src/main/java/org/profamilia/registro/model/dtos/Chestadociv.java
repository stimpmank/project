package org.profamilia.registro.model.dtos;

public class Chestadociv {
    private String cececodigo;
    private String ceccdescripcio;
    private String ceceestado;

    public Chestadociv() {
    }


    public void setCececodigo(String cececodigo) {
        this.cececodigo = cececodigo;
    }

    public String getCececodigo() {
        return cececodigo;
    }

    public void setCeccdescripcio(String ceccdescripcio) {
        this.ceccdescripcio = ceccdescripcio;
    }

    public String getCeccdescripcio() {
        return ceccdescripcio;
    }

    public void setCeceestado(String ceceestado) {
        this.ceceestado = ceceestado;
    }

    public String getCeceestado() {
        return ceceestado;
    }
}
